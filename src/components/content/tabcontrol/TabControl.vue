<template>
    <div class="tab-control">
        <div v-for="(item,index) in titles" :key="index" :class="{active: index === currenIndex}" @click="itemClick(index)">
            <span>{{item}}</span>
        </div>
    </div>
</template>

<script>
    export default {
        name: 'TabControl',
        props: {
            titles: {
                type: Array,
                default(){
                    return []
                }
            }
        },
        data(){
            return {
                currenIndex: 0
            }
        },
        methods:{
            itemClick(index){
                this.currenIndex = index;
                this.$emit('goodClick',index)
            }
        }
    }
</script>

